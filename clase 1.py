# un diagrama de clases se caracteriza por:
# - Clase
# - Atributos
# - Métodos
# - Relaciones

# Clase: es el molde de los objetos
# Atributos: son las características de los objetos
# Métodos: son las acciones que pueden realizar los objetos
# Relaciones: son las conexiones entre las clases

# Un diagrama de secuencia se caracteriza por:
# - Objetos
# - Mensajes
# - Líneas de vida

# Objetos: son las instancias de las clases
# Mensajes: son las acciones que se envían los objetos
# Líneas de vida: son las líneas verticales que representan la vida de los objetos

